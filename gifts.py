def reduce_to_single_digit(num):
    while num > 9:
        num = sum(int(d) for d in str(num))
    return num


def calculate_gift_code(day: int, month: int, year: int):
    first = reduce_to_single_digit(day + month)
    second = reduce_to_single_digit(year)
    third = reduce_to_single_digit(first + second)
    return f"{first}-{second}-{third}"


